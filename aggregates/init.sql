CREATE TABLE customer
(
  customer_id SERIAL PRIMARY KEY,
  title CHAR(4),
  fname VARCHAR(32),
  lname VARCHAR(32) NOT NULL,
  addressline VARCHAR(64),
  town VARCHAR(32),
  zipcode CHAR(10) NOT NULL,
  phone VARCHAR(16)
);

CREATE TABLE numbers
(
  ints INTEGER,
  floats REAL
);

